package src.baekjoon_10171;

public class Main {
    public static void main(String[] args) {
        System.out.println("\\    /\\");
        System.out.println(" )  ( ')");
        System.out.println("(  /  )");
        System.out.println(" \\(__)|");
        /*
        System.out.println 메소드로 \    /\ 출력
		\는 이스케이프 시퀀시로 사용되는 문자이기 때문에 출력하려면
		\\와같이 앞에 \를 하나 붙여야 정상적으로 출력된다.
		*/
    }
}